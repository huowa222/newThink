package com.lenovo.newThink.vo;

public class NewInspirationVo {
	private long id;
	private String content;
	private long newIdeaid;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public long getNewIdeaid() {
		return newIdeaid;
	}
	public void setNewIdeaid(long newIdeaid) {
		this.newIdeaid = newIdeaid;
	}

}
